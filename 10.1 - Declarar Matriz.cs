Declarar matrizes

Uma matriz é um tipo especial de variável que pode conter vários valores do mesmo tipo de dado.
A sintaxe da declaração é diferente por que é necessário especificar o tipo de dados e o tamanho da matriz.


Declarando uma nova matriz

Vamos declarar uma nova matriz string para conter três elementos.
*/

