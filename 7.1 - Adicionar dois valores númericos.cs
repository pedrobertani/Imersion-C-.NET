/*
Adição

Para trabalharmos com adição no C# utilizaremos o mesmo simbolo da concatenação, " + "
A línguagem C# utiliza os mesmos simbolos para algumas finalidades, isso se chama "sobrecarregar operador"
Na utitilização do simbolo + entre duas variaveis int o compilador entende que você está querendo fazer uma operação matemática
*/

int firstNumber = 5;
int secondNumber = 6;
Console.WriteLine(firstNumber + secondNumber);

/*
Saída
11
*/