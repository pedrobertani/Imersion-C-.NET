//*Sequencias de escape

//Pulando linhas
//O comando \n pula uma linha em um código

Console.WriteLine("Vamos pular uma linha! \n Agora temos um pulo.")

//Saída

//Vamos pular uma linha! 
//Agora temos um pulo.

//Assim, podemos fazer um texto em um unico console sem precisar usar o Console.WriteLine() em várias linhas de comando.

Console.WriteLine("Vamos pular uma linha!");
Console.WriteLine("Agora temos um pulo.");

//Saída
//Vamos pular uma linha!
//Agora temos um pulo.