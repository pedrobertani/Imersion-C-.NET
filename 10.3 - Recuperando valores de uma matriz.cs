/*
Recuperando valores de uma matriz

Para recuperarmos valores de uma matriz utilizaremos o indice adicionado.
*/

string[] fraudulentOrderIDs = new string[3];

fraudulentOrderIDs[0] = "A123";
fraudulentOrderIDs[1] = "B456";
fraudulentOrderIDs[2] = "C789";

Console.WriteLine($"Primeiro: {fraudulentOrderIDs[0]}");
Console.WriteLine($"Segundo: {fraudulentOrderIDs[1]}");
Console.WriteLine($"Terceiro: {fraudulentOrderIDs[2]}");

/*

Saída
Primeiro: A123
Segundo: B456
Terceiro: C789

*/
