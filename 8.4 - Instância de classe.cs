Criar uma instancia de uma classe

Uma instância de uma classe é chamada de objeto.
Para criarmos uma instância de uma classe, devemos utilizar o operador "new".
Considere a seguinte linha de código que cria uma instância Random para criar um objeto chamado "dice":

Random dice = new Random();

O operador new faz várias coisas importantes:

Primeiro, ele solicita um endereço na memória do computador
grande o suficiente para armazenar um novo objeto com base na classe Random.
Ele cria o objeto e o armazena no endereço de memória.
Ele retorna o endereço de memória para que ele possa ser salvo na variável dice.
E quando a variavel "dice" for chamada, o tempo de execução do .NET executa uma pesquisa nos bastidores
Para dar a ilusão de que você está trabalhando diretamente com o próprio objeto.


