//Variavel Implicita

//Uma variável local de tipo implícito é criada usando a palavra-chave var
//que instrui o compilador de C# a inferir o tipo.
//Depois de inferir o tipo, é como se o tipo de dados real tivesse sido usado para declarar a variável.
//Ou seja, ela identifica no modo codado qual tipo de variavel é.

var menssagem = "Hello World!";

//O copilador entende que a variavel "menssagem" é do tipo string, pois são caracteres em aspas duplas " ... "
//A palavra-chave var informa ao compilador para inferir o tipo de dados da variável com base no valor para o qual ele é inicializado.
//Você provavelmente verá a palavra-chave var enquanto lê o código de outras pessoas. No entanto, você deve usar o tipo de dados quando possível.