/*
 Literal cadeia textual

Nesse caso, teremos apenas um caracter especial que mantera todo nosso texto do jeito que é escrito.
Para fazermos isto devemos usar o @ antes do texto.
*/

Console.WriteLine(@"   c:\c#\Apostila   
      Aqui está o arquivo.");

/*
Saída

   c:\c#\Apostila   
      Aqui está o arquivo.
*/