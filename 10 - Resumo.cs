Resumo Matriz


O que é uma matriz

Matriz é uma sequência de elementos de dados individuais que pode ser acessada por meio de um única variável.
Utilizando um índice númerico que inicia-se no 0 em diante, para acessar cada elemento de uma matriz.
Ela nos permite coletar dados com uma finalidade ou característica em comum em uma única estrutura de dados para facilitar o processamento.

Uma matriz é uma variável especial que contém uma sequência de elementos de dados relacionados.
Você deve memorizar o formato básico de uma declaração de variável de matriz.
Acesse cada elemento de uma matriz para definir ou obter seus valores usando um índice baseado em zero dentro de colchetes.
Se você tentou acessar um índice fora dos limites da matriz, terá uma exceção de tempo de execução.
A propriedade Length é uma maneira programática de determinar o número de elementos em uma matriz.

Use a instrução foreach para interar em cada elemento de uma matriz,
executando o bloco de código associado uma vez para cada elemento da matriz.
A instrução foreach define o valor do elemento atual na matriz como uma variável temporária, 
que pode ser usada no corpo do bloco de código.
Use o operador de incremento ++ para adicionar 1 ao valor atual de uma variável.
*/
