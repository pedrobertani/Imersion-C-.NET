//Lógica de decisão if-elseif-else

//Use uma instrução if para ramificar sua lógica de código.
//A instrução de decisão if executará o código em seu bloco de código se sua expressão booliana for verdadeira.
//Caso contrário, o runtime ignorará o bloco de código e continuará para a próxima linha de código
//após o bloco de código.
//Uma expressão booliana é qualquer expressão que retorna um valor booliano.
//Os operadores boolianos comparam os dois valores
//à esquerda e à direita, em termos de igualdade, comparação e muito mais.
//Um bloco de código é definido por chaves { }.
//Ele coleta linhas de código que devem ser tratadas como uma única unidade.
//O operador lógico AND && agrega duas expressões de forma que as duas subexpressões devam ser verdadeiras para que toda a expressão seja verdadeira.
//O operador lógico OR || agrega duas expressões de forma que
//se alguma das subexpressões for verdadeira, toda a expressão será verdadeira.

//O if-else permite testar uma condição e executar código
//quando uma expressão booliana é verdadeira e um código diferente quando a expressão booliana é falsa.
//Você pode aninhar instruções 'if' para restringir uma condição possível.
//No entanto, considere usar a instrução if-else if-else em vez de fazer isso.
//Use else if para criar várias condições exclusivas.
