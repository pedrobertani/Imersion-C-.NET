//Recuperand variáveis

/*
Para recuperar um valor de uma variável, basta usar o nome da variável.
Este exemplo definirá o valor de uma variável e, em seguida, recuperará esse valor e o imprimirá no console.
*/

//atribuindo valor na variavel
string firstName;
firstName = "Pedro";
Console.WriteLine(firstName);

//Recuperar um valor de uma variável também é chamado de "obter a variável" ou, simplesmente, de uma operação "get".

//reatribuindo valores da variavel
string firstName;
firstName = "Bob";
Console.WriteLine(firstName);
firstName = "Beth";
Console.WriteLine(firstName);
firstName = "Conrad";
Console.WriteLine(firstName);
firstName = "Grant";
Console.WriteLine(firstName);

//Você deve definir uma variável como um valor antes de poder obter o valor da variável. Caso contrário, você verá um erro.
string firstName;
Console.WriteLine(firstName);

//Para evitar constrangimentos o correto é depois que declarar a variavel atribuir um valor assim que possível.

