Bloco de código

Um bloco de código é uma coleção de uma ou mais linhas de código definidas por um símbolo de chave de abertura e fechamento { }. 
Ele representa uma unidade de código completa com uma única finalidade em nosso sistema de software.
Nesse caso, no runtime, todas as linhas de código no bloco de código serão executadas se a expressão booliana for verdadeira.
Por outro lado, se a expressão booliana for falsa, todas as linhas de código no bloco de código serão ignoradas.
*/